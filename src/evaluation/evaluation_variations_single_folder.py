import os
import sys
PROJECT_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), os.pardir))
sys.path.append(PROJECT_ROOT)
from utils.split_files_into_groups import read_files_split_into_groups
from utils.verifinger_variations_evaluate import evaluate_variations_verifinger


def main():

    if ( len(sys.argv) < 3 ):
        print('Usage: python '+sys.argv[0]+ ' <input_dir>')
        print('\t input_dir: this folder should include all the images generated by gan')
        print('\t eval_dir: the results will be stored in this folder')
        sys.exit(0)

    # parse command line parameters
    input_dir =  sys.argv[1]

    eval_dir =  sys.argv[2]
    if not os.path.exists(eval_dir):
        os.makedirs(eval_dir)

    eval_file_path = eval_dir + '/latest.txt'

    # get the unique classes
    all_files = read_files_split_into_groups(input_dir)

    # call the evaluation function
    evaluate_variations_verifinger(input_dir, all_files, eval_file_path)


if __name__ == '__main__':
    main()